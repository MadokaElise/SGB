public class Personnage {
	
	
	int mana;
	int pvs;
	int force;
	int agilite;
	
	
	
	public Personnage(){
		
		mana=0;
		pvs=0;
		force=0;
		agilite=0;
	}

	
	public Personnage(int pmana, int ppvs, int pagilite, int pforce){
		
		mana=pmana;
		pvs=ppvs;
		force=pforce;
		agilite=pagilite;
		
	}
	
	
}

